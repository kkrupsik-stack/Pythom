import turtle

# Шаг 1: Создаём экран
screen = turtle.Screen()
screen.setup(500, 500)  # Размер окна

# Шаг 2: Создаём двух черепашек
t1 = turtle.Turtle()  # Первая черепашка (красная)
t2 = turtle.Turtle()  # Вторая черепашка (зелёная)

# Присваиваем цвета и форму
t1.color("red")
t2.color("green")

# Увеличиваем скорость для обеих черепашек
t1.speed(10)
t2.speed(10)

# Шаг 3: Обе черепашки рисуют половину окружности, чтобы получился круг
# Первая черепашка (красная) рисует верхнюю половину
t1.penup()
t1.goto(0, -100)  # Стартуем снизу от центра круга
t1.pendown()
t1.circle(100, 180)  # Рисуем дугу радиусом 100, угол 180° (полуокружность вверх)

# Вторая черепашка (зелёная) рисует нижнюю половину
t2.penup()
t2.goto(0, 100)  # Стартуем сверху от центра круга
t2.pendown()
t2.circle(100, -180)  # Рисуем дугу радиусом 100, угол -180° (полуокружность вниз)

# Шаг 4: Одна черепашка спускается сверху вниз (зелёная)
t2.left(90)  # Поворачиваем влево на 90°
t2.forward(100)  # Спускаемся вниз на 100 пикселей

# Рисуем зелёную звезду внизу
t2.dot(20, "green")  # Рисуем точку-звезду

# Шаг 5: Вторая черепашка (красная) двигается вниз на половину своей нарисованной окружности,
# поворачивает и двигается вперёд
t1.penup()
t1.goto(100, 0)  # Перемещаемся в правую точку окружности
t1.pendown()
t1.right(90)  # Поворачиваем направо на 90°
t1.forward(50)  # Двигаемся вниз на половину радиуса (50 пикселей)

# Рисуем красную звезду справа
t1.dot(20, "red")  # Рисуем точку-звезду

# Завершаем программу
turtle.done()
