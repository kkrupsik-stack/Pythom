# 1. Выводим условие задачи
print("Электронная книга по программированию на английском языке состоит из S страниц.")
print("В среднем на странице C строк по N символов.")
print("Используется кодировка UTF-8. Определите объём текстового файла в Кбайтах.\n")

# 2. Получаем данные от пользователя
s = int(input('Введите кол-во страниц: '))
c = int(input('Введите кол-во строк на странице: '))
n = int(input('Введите кол-во символов на странице: '))

# 3. Выводим условие с подставленными значениями
print(f"\nЭлектронная книга по программированию на английском языке состоит из {s} страниц.")
print(f"В среднем на странице {c} строк по {n} символов.")
print("Используется кодировка UTF-8. Определите объём текстового файла в Кбайтах.\n")

# 4. Решаем задачу с пошаговым выводом
print("Решение:")

# Действие 1: Находим общее количество символов
r1 = s * c * n
print(f"1) {s} * {c} * {n} = {r1} - всего символов в книге")

# Действие 2: Переводим в Кбайты (делим на 1024)
r2 = r1 / 1024
print(f"2) {r1} / 1024 = {r2} - Кбайт")

# 5. Выводим ответ
print(f"\nОтвет: {r2} Кбайт")
