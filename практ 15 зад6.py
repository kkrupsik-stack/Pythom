import turtle

# 1. Создаём экран и задаём цвет фона (например, светло-серый)
screen = turtle.Screen()
screen.bgcolor("lightgray")

# 2. Создаём объект «черепашка»
t = turtle.Turtle()
t.speed(10)  # Увеличиваем скорость рисования

# 3. Определяем массив с различными цветами
colors = ["red", "blue", "purple", "yellow", "green", "orange", "pink"]

# 4. Цикл for для рисования окружностей (диапазон от 0 до 12)
for i in range(12):
    # 5. Выбираем цвет по принципу: индекс цвета = i % количество цветов
    color_index = i % len(colors)
    t.pencolor(colors[color_index])  # Устанавливаем цвет пера

    # 6. Рисуем окружность радиусом 100 пикселей
    t.circle(100)

    # 7. Отступаем влево или вправо (поворачиваем на 30 градусов)
    t.left(30)  # Поворот против часовой стрелки

# Завершаем программу
turtle.done()

